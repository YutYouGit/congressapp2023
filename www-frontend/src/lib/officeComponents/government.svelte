<ul>
  <li>
    Board of County Commissioners - The board of county legislators that serve
    for 3 years.
  </li>
  <li>
    Board of Elections - Manages local and congressional elections, such as
    mayor, congressman, BOE, etc.
  </li>
  <li>Chief Financial Officer - Manages all finances in the county.</li>
  <li>Clerk of the Board - Runs agendas, like a secretary of the board.</li>
  <li>
    Communications - Takes care of public relations and diplomacy with other
    counties.
  </li>
</ul>

