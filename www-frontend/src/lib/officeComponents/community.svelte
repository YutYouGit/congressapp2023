<h1>
        Aging & Disabled Services - Public service for senior citizens and disabled people; provides free assistance for these groups of people. 
        County Clerk- Certification of passports and legal documents,certain elections, real estate transfer.
        Department of Community Services - Coordinates programs and services to meet basic human needs.
        Department of Transportation - Maintains & adds transportation networks to benefit the residents of the county, such as train stations, buses 
        Engineering - Gives engineering advice and assistance to all county departments.
        Information Technology - Offers technology for hardware and public services of other departments 
        Parks and Recreation - Manages public park and recreation areas in terms of cleanliness, safety, accessibility.
        Planning - Building and zoning of new property in towns.
        Public Works - Basic maintenance of roads, bridges, train tracks; also trash collection and sewer management.
        Sheriff - Basic courthouse management, manages jails and prisons, highest rank of law enforcement in a township.
        Surrogate - Manages actions that must be done in place of someone else such as minors, senior citizens, disabled citizens, dependents.
        
        
