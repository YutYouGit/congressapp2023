<h1>
        Board of County Commissioners - The board of county legislators that serve for 3 years. 
        Board of Elections - Manages local and congressional elections, such as mayor, congressman, BOE, etc. 
        Chief Financial Officer - Manages all finances in the county.   
        Clerk of the Board - Runs agendas, like a secretary of the board.
        Communications - Takes care of public relations and diplomacy with other counties.
        </h1>
        